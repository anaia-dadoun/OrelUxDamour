    private void tblVinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVinMouseClicked
        // TODO add your handling code here:
      this.fieldMillesime.setText("");
      this.fieldPrix.setText("");
      this.fieldCepage.setText("");
      this.fieldCouleur.setText("");
      
      int numBouteille = Integer.parseInt(this.tblVin.getValueAt(this.tblVin.getSelectedRow(),0).toString());
      
      int monMillesime=0;
      double monPrix=0;
      String monCepage="";
      String maCouleur="";
      
      
      for(Bouteille bout: mesBouteilles)
      {
          if(bout.getIdBouteille()==numBouteille)
          {
              monMillesime=bout.getLeVin().getMillesimeVin();
              monPrix=bout.getLeVin().getPrixduVin();
              monCepage=bout.getLeVin().getNomVin();
              maCouleur=bout.getLeVin().getLaCouleur().getUneCouleur();
                      
                     
          }
      }
      
        this.fieldMillesime.setText(String.valueOf(monMillesime));
        this.fieldPrix.setText(String.valueOf(monPrix));
        this.fieldCepage.setText(String.valueOf(monCepage));
        this.fieldCouleur.setText(String.valueOf(maCouleur));
        
        
        
        
        
    }//GEN-LAST:event_tblVinMouseClicked

