    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
         DefaultTableModel dtmVin = new DefaultTableModel();
        dtmVin = (DefaultTableModel) tblVin.getModel();
        Vector v;
        
        Couleur blanc = new Couleur("Blanc");
        Couleur rouge = new Couleur("Rouge");
        
        
        Cepage beaujolais = new Cepage("Beaujolais");
        Cepage Sauvignon = new Cepage("Sauvignon");
        
        Vin vin1 = new Vin("Julienas", 2009, Sauvignon, blanc,19);
        Vin vin2 = new Vin("Morgon", 2011, beaujolais, rouge,8);
        Vin vin3 = new Vin("Champagne", 2005, Sauvignon, blanc,15);
        
        Bouteille b1 = new Bouteille(1, 5, vin1);
        Bouteille b2 = new Bouteille(2, 5, vin2);
        Bouteille b3 = new Bouteille(3, 10, vin3);
        
        mesBouteilles = new ArrayList<>();
        
        mesBouteilles.add(b3);
        mesBouteilles.add(b2);
        mesBouteilles.add(b1);
        
        double caveValueD=0;
        int nbrRouge = 0;
        int nbrBouteilleTotale=0;
        int nbrBlanc=0;

        for(Bouteille bout : mesBouteilles)
        {
            v=new Vector();
            v.add(bout.getIdBouteille());
            v.add(bout.getLeVin().getNomVin());
            v.add(bout.getQuantiteBouteille());
            dtmVin.addRow(v);
            caveValueD += bout.getQuantiteBouteille()*bout.getLeVin().getPrixduVin();
            
            nbrBouteilleTotale = nbrBouteilleTotale+ bout.getQuantiteBouteille();
            
            if(bout.getLeVin().getLaCouleur().getUneCouleur()== "Rouge"){
                nbrRouge = nbrRouge+bout.getQuantiteBouteille();
            }
            if(bout.getLeVin().getLaCouleur().getUneCouleur() == "Blanc"){
                nbrBlanc = nbrBlanc+bout.getQuantiteBouteille();
            }
            
            
        }
        double pourcentRouge = ((double)nbrRouge/(double)nbrBouteilleTotale)*100;
        System.out.println(pourcentRouge);
        caveValue.setText(String.valueOf(caveValueD));
        pourcentageRouge.setText(String.valueOf(pourcentRouge)+"%");
        
         double pourcentBlanc = ((double)nbrBlanc/(double)nbrBouteilleTotale)*100;
        System.out.println(pourcentBlanc);
        caveValue.setText(String.valueOf(caveValueD));
        pourcentageBlanc.setText(String.valueOf(pourcentBlanc)+"%");
        
        
        
        
        
        
        
    }//GEN-LAST:event_formWindowOpened

